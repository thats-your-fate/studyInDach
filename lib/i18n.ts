export type PublicLocale = "en" | "pt-br" | "es"

export function dbTranslationLocale(locale: PublicLocale) {
	if (locale === "pt-br") return "pt"
	if (locale === "es") return "es"
	return "en"
}

export function isPortuguese(locale: PublicLocale) {
	return locale === "pt-br"
}

export function localePrefix(locale: PublicLocale) {
	if (locale === "pt-br") return "/pt-br"
	if (locale === "es") return "/es"
	return ""
}

